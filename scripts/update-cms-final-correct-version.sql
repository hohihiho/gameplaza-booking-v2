## 제1장 총칙

### 제1조 (목적)
이 약관은 광주 게임플라자가 제공하는 게임기기 예약 서비스를 이용함에 있어 회사와 이용자의 권리, 의무 및 책임사항을 규정함을 목적으로 합니다.

### 제2조 (정의)
1. "서비스"란 회사가 제공하는 게임기기 예약 및 관련 제반 서비스를 의미합니다.
2. "이용자"란 이 약관에 따라 회사가 제공하는 서비스를 받는 회원 및 비회원을 말합니다.
3. "회원"이란 회사와 서비스 이용계약을 체결하고 이용자 아이디를 부여받은 이용자를 말합니다.
4. "비회원"이란 회원가입 없이 회사가 제공하는 제한된 서비스를 이용하는 자를 말합니다.
5. "예약"이란 특정 시간대에 게임기기를 이용하기 위해 사전에 신청하는 행위를 말합니다.
6. "노쇼(No-Show)"란 예약 후 사전 취소 없이 이용하지 않는 행위를 말합니다.
7. "체크인"이란 예약한 시간에 실제로 도착하여 이용을 시작하는 것을 말합니다.

### 제3조 (약관의 효력 및 변경)
1. 이 약관은 서비스 화면에 게시하거나 기타의 방법으로 회원에게 공지함으로써 효력이 발생합니다.
2. 회사는 필요한 경우 관련 법령을 위배하지 않는 범위에서 이 약관을 변경할 수 있습니다.
3. 약관이 변경되는 경우 회사는 변경사항을 시행일자 7일 전부터 공지합니다.
4. 회원이 변경된 약관에 동의하지 않는 경우 서비스 이용을 중단하고 탈퇴할 수 있습니다.

## 제2장 회원가입 및 서비스 이용

### 제4조 (회원가입)
1. 회원가입은 이용자가 이 약관에 동의하고 회사가 정한 절차에 따라 회원정보를 입력한 후 회사가 이를 승인함으로써 성립됩니다.
2. 회사는 다음 각 호에 해당하는 경우 회원가입을 거부하거나 사후에 회원자격을 제한할 수 있습니다:
   - 타인의 명의를 도용한 경우
   - 허위 정보를 기재한 경우
   - 만 14세 미만인 경우
   - 이전에 회원자격을 상실한 적이 있는 경우
   - 기타 회사가 정한 이용신청 요건이 미비한 경우

### 제5조 (회원정보의 변경)
1. 회원은 서비스 이용 중 본인의 정보가 변경된 경우 즉시 수정해야 합니다.
2. 회원정보 미변경으로 인한 불이익은 회원이 부담합니다.

### 제6조 (회원탈퇴 및 자격 상실)
1. 회원은 언제든지 탈퇴를 요청할 수 있으며, 회사는 즉시 회원탈퇴를 처리합니다.
2. 회사는 다음 각 호의 사유가 발생한 경우 회원자격을 제한하거나 상실시킬 수 있습니다:
   - 가입 시 허위 내용을 등록한 경우
   - 다른 사람의 서비스 이용을 방해하거나 정보를 도용하는 경우
   - 서비스를 이용하여 법령 또는 이 약관이 금지하는 행위를 하는 경우
   - 기타 회원으로서의 자격을 지속시키는 것이 부적절하다고 판단되는 경우

## 제3장 예약 서비스

### 제7조 (예약 규정)
1. 회원은 서비스를 통해 게임기기를 예약할 수 있습니다.
2. 예약은 선착순으로 진행되며, 1인당 예약 가능한 횟수와 시간이 제한될 수 있습니다.

### 제8조 (예약 취소)
1. 예약 취소는 예약 시간 24시간 전까지 가능합니다.
2. 무단 불참(노쇼) 시 향후 예약에 제한이 있을 수 있습니다.

### 제9조 (체크인 및 이용)
1. 예약자는 예약 시간에 맞춰 체크인을 진행해야 합니다.
2. 체크인하지 않은 예약은 노쇼로 처리될 수 있습니다.

### 제10조 (노쇼 관리)
무단 불참(노쇼)이 반복되는 경우, 회사는 관리자 재량으로 해당 회원의 예약 권한을 제한할 수 있습니다.

## 제4장 서비스 이용

### 제11조 (서비스의 제공)
1. 회사는 다음과 같은 서비스를 제공합니다:
   - 게임기기 예약 서비스
   - 예약 확인 및 변경 서비스
   - 이용 내역 조회 서비스
   - 공지사항 및 이벤트 정보 제공
   - 기타 회사가 정하는 서비스
2. 서비스는 연중무휴, 24시간 제공을 원칙으로 합니다. 단, 시설 점검 등의 사유로 일시 중단될 수 있습니다.

### 제12조 (서비스의 변경 및 중단)
1. 회사는 운영상, 기술상의 필요에 따라 서비스를 변경할 수 있습니다.
2. 다음 각 호의 경우 서비스 제공을 일시적으로 중단할 수 있습니다:
   - 시스템 정기점검, 증설 및 교체의 경우
   - 네트워크 장애 또는 서비스 이용 폭주로 정상적인 서비스 제공이 어려운 경우
   - 천재지변, 비상사태 등 불가항력적 사유가 있는 경우

### 제13조 (정보의 제공 및 광고)
1. 회사는 서비스 이용에 필요한 정보를 공지사항, 이메일 등의 방법으로 회원에게 제공할 수 있습니다.
2. 회사는 서비스 제공과 관련하여 서비스 화면, 이메일 등에 광고를 게재할 수 있습니다.
3. 회원은 회사가 제공하는 광고에 대한 수신 거부 의사를 밝힐 수 있습니다.

## 제5장 계약 당사자의 의무

### 제14조 (회사의 의무)
1. 회사는 법령과 이 약관이 금지하거나 공서양속에 반하는 행위를 하지 않습니다.
2. 회사는 지속적이고 안정적인 서비스 제공을 위해 노력합니다.
3. 회사는 회원의 개인정보를 보호하기 위해 보안시스템을 구축하며 개인정보처리방침을 공시하고 준수합니다.
4. 회사는 회원으로부터 제기되는 의견이나 불만이 정당하다고 인정할 경우 이를 처리합니다.

### 제15조 (회원의 의무)
1. 회원은 다음 행위를 하여서는 안 됩니다:
   - 신청 또는 변경 시 허위 내용의 등록
   - 타인의 정보 도용
   - 회사가 게시한 정보의 변경
   - 회사가 금지한 정보의 송신 또는 게시
   - 회사 및 기타 제3자의 저작권 등 지적재산권 침해
   - 회사 및 기타 제3자의 명예를 손상시키거나 업무를 방해하는 행위
   - 외설 또는 폭력적인 내용을 전송 또는 게시하는 행위
   - 기타 불법적이거나 부당한 행위
2. 회원은 관계법령, 이 약관, 이용안내 및 서비스와 관련하여 공지한 주의사항을 준수해야 합니다.
3. 회원은 게임기기를 소중히 다루어야 하며, 고의 또는 과실로 인한 파손 시 배상책임이 있습니다.

